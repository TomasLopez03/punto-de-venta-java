    private void ingresarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingresarBtnActionPerformed
        // TODO add your handling code here:
        String sql = "select * from productos where codigo = ?";
        int cantidad = Integer.parseInt(txtcantidad.getText());
        try{
            con = cn.getConnection();
            ps = con.prepareStatement(sql);
            ps.setString(1, txtcodigo.getText());
            rs = ps.executeQuery();
            if(rs.next()){
                prod.setId(rs.getInt("id_producto"));
                prod.setCodigo(rs.getString("codigo"));
                prod.setDescripcion(rs.getString("descripcion"));
                prod.setPrecio(rs.getDouble("precio"));
                prod.setStock(rs.getInt("stock_disponible"));
            }
            modelo = (DefaultTableModel) tableVenta.getModel();
            Object[] fila = new Object[5];
            fila[0] = prod.getCodigo();
            fila[1] = prod.getDescripcion();
            fila[2] = cantidad;
            fila[3] = prod.getPrecio();
            fila[4] = prod.getPrecio()*cantidad;
            modelo.addRow(fila);
            total = prod.getPrecio()*cantidad + total;
            lbltotal.setText(String.valueOf("$"+total));
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Error, Producto inexistente!!");
        }
    }//GEN-LAST:event_ingresarBtnActionPerformed
