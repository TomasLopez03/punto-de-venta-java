    private void updateProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateProdActionPerformed
        int fila = tableProd.getSelectedRow();
        modelo = (DefaultTableModel) tableProd.getModel();
        String sql2="select id_producto from productos where codigo = ?";
        if(fila!=-1){
            try {
                con = cn.getConnection();
                Object codigo = modelo.getValueAt(fila,0);
                ps = con.prepareStatement(sql2);
                ps.setObject(1, codigo);
                rs = ps.executeQuery();
                if(rs.next()){
                    prod.setId(rs.getInt("id_producto"));
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,ex.toString());
            }
            String sql = "update productos set codigo=?, descripcion=?, precio=?, stock_disponible=? where id_producto=?";
            prod.setCodigo(String.valueOf( modelo.getValueAt(fila,0)));
            prod.setDescripcion(String.valueOf(modelo.getValueAt(fila,1)));
            prod.setPrecio(Double.valueOf(String.valueOf(modelo.getValueAt(fila,2))));
            prod.setStock(Integer.parseInt(String.valueOf( modelo.getValueAt(fila,3))));
            try{
                con = cn.getConnection();
                ps = con.prepareStatement(sql);
                ps.setString(1, prod.getCodigo());
                ps.setString(2, prod.getDescripcion());
                ps.setDouble(3, prod.getPrecio());
                ps.setInt(4, prod.getStock());
                ps.setInt(5, prod.getId());
                ps.execute();
            }catch(SQLException e){
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error al actualizar");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fila");
        }
        String sql3 = "select codigo,descripcion,precio,stock_disponible from productos";
        mostrarDatos(modelo,sql3);
    }//GEN-LAST:event_updateProdActionPerformed

