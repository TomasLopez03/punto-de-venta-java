    public void eliminar(DefaultTableModel modelo,String sql,int fila){
        if(fila!=-1){
            Object id = modelo.getValueAt(fila,0);
            int confirmacion = JOptionPane.showConfirmDialog(null,"Est seguro de eliminar este cliente?","Confirmar eliminacin",JOptionPane.YES_NO_OPTION);
            if(confirmacion == JOptionPane.YES_OPTION){
                try{
                    con = cn.getConnection();
                    ps = con.prepareStatement(sql);
                    ps.setObject(1,id);
                    int filaAfectada = ps.executeUpdate();
                    if(filaAfectada>0){
                        modelo.removeRow(fila);
                        JOptionPane.showMessageDialog(null, "Fila eliminada correctamente");
                    }else{
                        JOptionPane.showMessageDialog(null, "Error al eliminar la fila de la base de datos");
                    }
                }catch(SQLException e){
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(null, "Error al conectar con la base de datos");
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "Por favor, selecciona una fila para eliminar");
        }
    }
    
    
